package myPkg.board;

public class BoardDTO {

	private int num;
	private String writer;
	private String subject;
	private String bfile;
	private String content;
	private String reg_date;
	private int count;
	private String re_writer;
	private String re_content;
	private String re_reg_date;
	
	public int getNum() {
		return num;
	}
	
	public void setNum(int num) {
		this.num = num;
	}
	
	public String getWriter() {
		return writer;
	}
	
	public void setWriter(String writer) {
		this.writer = writer;
	}
	
	public String getSubject() {
		return subject;
	}
	
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	public String getBfile() {
		return bfile;
	}
	
	public void setBfile(String bfile) {
		this.bfile = bfile;
	}
	
	public String getContent() {
		return content;
	}
	
	public void setContent(String content) {
		this.content = content;
	}
	
	public String getReg_date() {
		return reg_date;
	}
	
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getRe_writer() {
		return re_writer;
	}
	
	public void setRe_writer(String re_writer) {
		this.re_writer = re_writer;
	}
	
	public String getRe_content() {
		return re_content;
	}
	
	public void setRe_content(String re_content) {
		this.re_content = re_content;
	}
	
	public String getRe_reg_date() {
		return re_reg_date;
	}
	
	public void setRe_reg_date(String re_reg_date) {
		this.re_reg_date = re_reg_date;
	}

}
